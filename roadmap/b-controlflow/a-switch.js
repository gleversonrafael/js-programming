// switch
const number = 0;
let alertMessage;

switch(number) {
     case 0:
          alertMessage = 0;
          break;

     case 1:
          alertMessage = 1;
          break;

     case 2:
     case 3:
          alertMessage = "2, 3"
          break;
          
     default:
}

console.log(alertMessage);